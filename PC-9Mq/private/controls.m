function [dE,dA,dR,T] = controls(t)

  % Chandelle
  dE = ((t>=6.) - (t>15.5))*(-3.)*pi/180.;
  dE += ((t>=15.5) - (t>18.))*(-2.)*pi/180.;
  dA = ((t>=5.) - (t>6.))*(-1.)*pi/180.;
  dA += ((t>=15.5) - (t>18.))*(+2.25)*pi/180.;
  dR = 0.;
  T = ((t>=5.) - (t>30.))*(6100.);

%{
  % Immelmann
  dE = ((t>=5.) - (t>10.))*(-7.)*pi/180.;
  dA = ((t>=10.) - (t>11.))*(-12.)*pi/180.;
  dR = 0.;
  T = ((t>=5.) - (t>30.))*(6100.);
%}
%{
  % Split S
  dE = ((t>=6.) - (t>12.))*(-4.5)*pi/180.;
  dA = ((t>=5.) - (t>6.))*(-5.4)*pi/180.;
  dR = 0.;
  T = ((t>=5.) - (t>30.))*(6100.);
%}
%{
  % Chandelle
  dE = ((t>=6.) - (t>15.5))*(-3.)*pi/180.;
  dE += ((t>=15.5) - (t>18.))*(-2.)*pi/180.;
  dA = ((t>=5.) - (t>6.))*(-1.)*pi/180.;
  dA += ((t>=15.5) - (t>18.))*(+2.25)*pi/180.;
  dR = 0.;
  T = ((t>=5.) - (t>30.))*(6100.);
%}
%{
  % Loop
  dE = ((t>=5.) - (t>21.))*(-5.)*pi/180.;
  dA = 0.;
  dR = 0.;
  T = ((t>=5.) - (t>30.))*(6100.);
%}
%{
  % Spin entry (enable the AOA limiter, initial speed 40 m/s)
  dE = ((t>=5.) - (t>25.))*(-15.)*pi/180.;
  dA = ((t>=0.) - (t>25.))*(-2.5)*pi/180.;
  dR = 0.;
  T = 0.;
%}
end
